import 'package:flutter/material.dart';
import 'package:http/http.dart' as http;
import 'dart:convert';
import 'series_screen.dart';
import '../../services/student_api_service.dart';

class ClassesScreen extends StatefulWidget {
  final int levelId;
  final String levelName;
  final String sectionName;

  const ClassesScreen({
    super.key,
    required this.levelId,
    required this.levelName,
    required this.sectionName,
  });

  @override
  State<ClassesScreen> createState() => _ClassesScreenState();
}

class _ClassesScreenState extends State<ClassesScreen> {
  List<dynamic> classes = [];
  bool isLoading = true;
  String? errorMessage;
  final StudentApiService _apiService = StudentApiService();

  @override
  void initState() {
    super.initState();
    fetchClasses();
  }

  Future<void> fetchClasses() async {
    print('');
    print('üöÄ ========== D√âBUT R√âCUP√âRATION DES CLASSES ==========');
    print('üìç Level ID: ${widget.levelId}');
    print('üìö Niveau: ${widget.levelName}');
    print('üîÑ Appel de l\'API...');
    
    try {
      final response = await _apiService.getClassesByLevel(widget.levelId);
      
      if (response.success) {
        print('');
        print('üìä ========== ANALYSE DE LA R√âPONSE ==========');
        print('‚úÖ R√©ponse re√ßue: SUCC√àS');
        print('üìã Donn√©es re√ßues: ${response.data}');
        
        final classesList = response.data as List<dynamic>;
        print('üìä Nombre de classes trouv√©es: ${classesList.length}');
        
        // Log de chaque classe trouv√©e
        for (int i = 0; i < classesList.length; i++) {
          final schoolClass = classesList[i];
          print('üéØ Classe ${i + 1}:');
          print('   üÜî ID: ${schoolClass['id']}');
          print('   üìö Nom: ${schoolClass['name']}');
          print('   üè´ Level ID: ${schoolClass['level_id']}');
        }
        
        setState(() {
          classes = classesList;
          isLoading = false;
        });
        
        print('');
        print('‚úÖ ========== CHARGEMENT R√âUSSI ==========');
        print('üéâ ${classesList.length} classes charg√©es avec succ√®s!');
        
      } else {
        print('');
        print('‚ùå ========== ERREUR API ==========');
        print('üî¥ Message d\'erreur: ${response.message}');
        
        setState(() {
          errorMessage = response.message;
          isLoading = false;
        });
      }
    } catch (e) {
      print('');
      print('üí• ========== EXCEPTION ATTRAP√âE ==========');
      print('üî¥ Erreur: $e');
      
      setState(() {
        errorMessage = 'Erreur lors du chargement des classes: $e';
        isLoading = false;
      });
    }
  }

  Color _getClassColor(int index) {
    List<Color> colors = [
      Colors.indigo[400]!,
      Colors.cyan[400]!,
      Colors.amber[600]!,
      Colors.deepPurple[400]!,
      Colors.green[500]!,
    ];
    return colors[index % colors.length];
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      body: Container(
        decoration: BoxDecoration(
          gradient: LinearGradient(
            begin: Alignment.topLeft,
            end: Alignment.bottomRight,
            colors: [
              Colors.green[800]!,
              Colors.green[600]!,
              Colors.green[400]!,
            ],
          ),
        ),
        child: SafeArea(
          child: Column(
            children: [
              // Header
              Padding(
                padding: const EdgeInsets.all(20.0),
                child: Row(
                  children: [
                    IconButton(
                      onPressed: () {
                        Navigator.of(context).pop();
                      },
                      icon: const Icon(
                        Icons.arrow_back_ios,
                        color: Colors.white,
                        size: 24,
                      ),
                    ),
                    const SizedBox(width: 10),
                    Expanded(
                      child: Column(
                        crossAxisAlignment: CrossAxisAlignment.start,
                        children: [
                          Text(
                            'Classes - ${widget.levelName}',
                            style: const TextStyle(
                              fontSize: 22,
                              fontWeight: FontWeight.bold,
                              color: Colors.white,
                            ),
                          ),
                          Text(
                            '${widget.sectionName} ‚Ä¢ S√©lectionnez la classe',
                            style: const TextStyle(
                              fontSize: 14,
                              color: Colors.white70,
                            ),
                          ),
                        ],
                      ),
                    ),
                  ],
                ),
              ),

              // Content
              Expanded(
                child: Container(
                  margin: const EdgeInsets.only(top: 20),
                  decoration: const BoxDecoration(
                    color: Colors.white,
                    borderRadius: BorderRadius.only(
                      topLeft: Radius.circular(30),
                      topRight: Radius.circular(30),
                    ),
                  ),
                  child: isLoading
                      ? const Center(
                          child: CircularProgressIndicator(
                            color: Colors.green,
                          ),
                        )
                      : errorMessage != null
                          ? _buildErrorWidget()
                          : classes.isEmpty
                              ? _buildEmptyWidget()
                              : ListView.builder(
                                  padding: const EdgeInsets.all(24),
                                  itemCount: classes.length,
                                  itemBuilder: (context, index) {
                                    final schoolClass = classes[index];
                                    final color = _getClassColor(index);

                                    return Padding(
                                      padding: const EdgeInsets.only(bottom: 16),
                                      child: _ClassCard(
                                        title: schoolClass['name'] ?? 'Classe sans nom',
                                        studentCount: 0, // Les donn√©es r√©elles n√©cessiteront un appel API s√©par√©
                                        seriesCount: 0,  // Les donn√©es r√©elles n√©cessiteront un appel API s√©par√©
                                        color: color,
                                        onTap: () {
                                          print('');
                                          print('üéØ ========== NAVIGATION VERS LES S√âRIES ==========');
                                          print('üìö Classe s√©lectionn√©e: ${schoolClass['name']}');
                                          print('üÜî ID de la classe: ${schoolClass['id']}');
                                          print('üîÑ Redirection vers SeriesScreen...');
                                          print('üìç Endpoint suivant: /api/mobile/classes/${schoolClass['id']}/series');
                                          
                                          Navigator.of(context).push(
                                            MaterialPageRoute(
                                              builder: (context) => SeriesScreen(
                                                classId: schoolClass['id'],
                                                className: schoolClass['name'],
                                                levelName: widget.levelName,
                                                sectionName: widget.sectionName,
                                              ),
                                            ),
                                          );
                                        },
                                      ),
                                    );
                                  },
                                ),
                ),
              ),
            ],
          ),
        ),
      ),
    );
  }

  Widget _buildErrorWidget() {
    return Center(
      child: Column(
        mainAxisAlignment: MainAxisAlignment.center,
        children: [
          Icon(
            Icons.error_outline,
            size: 64,
            color: Colors.red[400],
          ),
          const SizedBox(height: 16),
          Text(
            'Erreur de chargement',
            style: TextStyle(
              fontSize: 18,
              fontWeight: FontWeight.bold,
              color: Colors.grey[800],
            ),
          ),
          const SizedBox(height: 8),
          Padding(
            padding: const EdgeInsets.symmetric(horizontal: 32),
            child: Text(
              errorMessage!,
              textAlign: TextAlign.center,
              style: TextStyle(
                color: Colors.grey[600],
              ),
            ),
          ),
          const SizedBox(height: 24),
          ElevatedButton.icon(
            onPressed: () {
              setState(() {
                isLoading = true;
                errorMessage = null;
              });
              fetchClasses();
            },
            icon: const Icon(Icons.refresh),
            label: const Text('R√©essayer'),
            style: ElevatedButton.styleFrom(
              backgroundColor: Colors.green,
              foregroundColor: Colors.white,
            ),
          ),
        ],
      ),
    );
  }

  Widget _buildEmptyWidget() {
    return Center(
      child: Column(
        mainAxisAlignment: MainAxisAlignment.center,
        children: [
          Icon(
            Icons.class_,
            size: 64,
            color: Colors.grey[400],
          ),
          const SizedBox(height: 16),
          Text(
            'Aucune classe disponible',
            style: TextStyle(
              fontSize: 18,
              fontWeight: FontWeight.bold,
              color: Colors.grey[600],
            ),
          ),
          const SizedBox(height: 8),
          Text(
            'Il n\'y a pas de classes configur√©es\npour ce niveau.',
            textAlign: TextAlign.center,
            style: TextStyle(
              color: Colors.grey[500],
            ),
          ),
        ],
      ),
    );
  }
}

class _ClassCard extends StatelessWidget {
  final String title;
  final int studentCount;
  final int seriesCount;
  final Color color;
  final VoidCallback onTap;

  const _ClassCard({
    required this.title,
    required this.studentCount,
    required this.seriesCount,
    required this.color,
    required this.onTap,
  });

  @override
  Widget build(BuildContext context) {
    return InkWell(
      onTap: onTap,
      borderRadius: BorderRadius.circular(16),
      child: Container(
        padding: const EdgeInsets.all(20),
        decoration: BoxDecoration(
          color: Colors.white,
          borderRadius: BorderRadius.circular(16),
          border: Border.all(
            color: color.withOpacity(0.3),
            width: 2,
          ),
          boxShadow: [
            BoxShadow(
              color: color.withOpacity(0.1),
              blurRadius: 10,
              offset: const Offset(0, 5),
            ),
          ],
        ),
        child: Row(
          children: [
            Container(
              width: 60,
              height: 60,
              decoration: BoxDecoration(
                color: color.withOpacity(0.15),
                borderRadius: BorderRadius.circular(12),
              ),
              child: Icon(
                Icons.class_,
                size: 28,
                color: color,
              ),
            ),
            const SizedBox(width: 20),
            Expanded(
              child: Column(
                crossAxisAlignment: CrossAxisAlignment.start,
                children: [
                  Text(
                    title,
                    style: TextStyle(
                      fontSize: 18,
                      fontWeight: FontWeight.bold,
                      color: Colors.grey[800],
                    ),
                  ),
                  const SizedBox(height: 8),
                  Row(
                    children: [
                      if (studentCount > 0) ...[
                        Icon(
                          Icons.people,
                          size: 16,
                          color: Colors.grey[600],
                        ),
                        const SizedBox(width: 4),
                        Text(
                          '$studentCount √©l√®ves',
                          style: TextStyle(
                            fontSize: 13,
                            color: Colors.grey[600],
                          ),
                        ),
                        if (seriesCount > 0) const SizedBox(width: 16),
                      ],
                      if (seriesCount > 0) ...[
                        Icon(
                          Icons.format_list_bulleted,
                          size: 16,
                          color: Colors.grey[600],
                        ),
                        const SizedBox(width: 4),
                        Text(
                          '$seriesCount s√©rie${seriesCount > 1 ? 's' : ''}',
                          style: TextStyle(
                            fontSize: 13,
                            color: Colors.grey[600],
                          ),
                        ),
                      ],
                      if (studentCount == 0 && seriesCount == 0) ...[
                        Icon(
                          Icons.info_outline,
                          size: 16,
                          color: Colors.grey[600],
                        ),
                        const SizedBox(width: 4),
                        Text(
                          'Appuyez pour voir les s√©ries',
                          style: TextStyle(
                            fontSize: 13,
                            color: Colors.grey[600],
                          ),
                        ),
                      ],
                    ],
                  ),
                ],
              ),
            ),
            Icon(
              Icons.arrow_forward_ios,
              color: color,
              size: 20,
            ),
          ],
        ),
      ),
    );
  }
}
